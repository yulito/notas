    private void resultadoBusqueda(String ba){
        llenarTabla(ba);
    }
    
    private void llenarTabla(String bApunte){
        if(!this.itemCbx.isEmpty()){
            buscadorApunte.setEnabled(true);
            btnApunteAgregar.setEnabled(true);
            //obtenemos los datos de la tabla
            actrl = new ApunteControlador();
            ArrayList<ApunteModel> arr = actrl.mostrarApuntes(this.itemCbx,bApunte);
            //limpiar tabla
            limpiarTablaApuntes();
            //llenar tabla
            DefaultTableModel tb=(DefaultTableModel)tablaApuntes.getModel();
            for(ApunteModel aa:arr){
                tb.addRow(new Object[]{aa.getTitulo(), aa.getFecha()});
            }
        }
    }
    
    private void llenarItems(){
        tctrl = new TemaControlador();
        ArrayList<TemaModel> arr = tctrl.obtenerTemas();        
        //listar
        comboboxTema.addItem("Selecciona el Tema");
        for(TemaModel tt:arr){
            comboboxTema.addItem(tt.getTema());
        }
    }
    
    private void limpiarTablaApuntes(){
        DefaultTableModel tb =(DefaultTableModel)tablaApuntes.getModel(); 
        for(int i=tb.getRowCount()-1;i>=0;i--) 
            tb.removeRow(i);
    }
    
    
