    public void eliminarApunte(){
    
        JMenuItem eliminar = new JMenuItem("Eliminar");
        jPopupMenu1.add(eliminar);
        tablaApuntes.setComponentPopupMenu(jPopupMenu1);
        
        eliminar.addActionListener(new ActionListener(){   //indica que cuando presionemos el itme de eliminar en el popupmenu este debe de realizar una accion.
            @Override
            public void actionPerformed(ActionEvent e){                 
                try {
                    if(new ApunteControlador().eliminarApunte(tablaApuntes.getValueAt(tablaApuntes.getSelectedRow(), 0).toString()))
                    {
                        JOptionPane.showMessageDialog(null, "Se ha eliminado correctamente!");
                        llenarTabla("");
                    }else{
                        JOptionPane.showMessageDialog(null, "Ha ocurrido un error en la operaci√≥n.");                
                    }} catch (SQLException ex) {
                    Logger.getLogger(Principal.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }); 
    }
    
    
